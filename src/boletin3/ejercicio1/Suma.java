package boletin3.ejercicio1;

public class Suma {
	
	/**
	 * Devuelve la suma de dos numeros enteros
	 * 
	 * @param numero1 Numero 1
	 * @param numero2 Numero 2
	 * @return Suma de enteros
	 */
	static int suma(int numero1, int numero2){
		int suma;
		
		suma = numero1 + numero2;
		
		return suma;
	}
	
	/**
	 * Devuelve la suma de do numeros decimales 
	 * 
	 * @param numero1 Numero 1
	 * @param numero2 Numero 2
	 * @return Suma de decimales
	 */
	static double suma(double numero1, double numero2){
		double sumaDecimales;
		
		sumaDecimales = numero1 + numero2;
		
		return sumaDecimales;
	}
}
